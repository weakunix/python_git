const int N = 2e5;

int t, n, arr[N], mn = 1e9, ans = 0;

void subcase() {
    ans = 0;
    mn = 1e9;

    cin >> n;
    for (int i = 0; i < n; i++) cin >> arr[i];
    for (int i = 0; i < n; i++) cin >> arr[i];
    for (int i = n - 1; i >= 0; i--) {
        if (arr[i] <= mn) {
            ans++;
            mn = arr[i];
        }
    }
    cout << ans << "\n";
	return;
}

int main() {
	ios_base::sync_with_stdio(false);
	cin.tie(0), cout.tie(0);
	cin >> t;
	for (int i = 0; i < t; i++) subcase();
	return 0;
}
