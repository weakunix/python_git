#define sec second.first
#define third second.second
#define all(v) v.begin(), v.end()
#define rall(v) v.rbegin(), v.rend()

const int modval = 1e9 + 7;

int n;
vector<int> dp;

int main() {
    cin >> n;
    dp.resize(n + 1);
    dp[0] = 1;
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= 6; j++) {
            if (i - j >= 0) dp[i] = (dp[i] + dp[i - j]) % modval;
        }
    }
    cout << dp[n] << "\n";
	return 0;
}
